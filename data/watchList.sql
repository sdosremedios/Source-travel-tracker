-- View: series
DROP VIEW IF EXISTS series;
CREATE VIEW series AS
    SELECT s.name,
           s.rating,
           s.status,
           y.source,
           y.season,
           y.episodes,
           y.beginDate,
           y.note,
           s.url,
           s.IMDb
      FROM season AS y
           LEFT JOIN
           show AS s ON s.showID = y.showID
     ORDER BY s.rating DESC,
              s.name,
              y.season DESC;


-- View: vIMDB
DROP VIEW IF EXISTS vIMDB;
CREATE VIEW vIMDB AS
    SELECT s.name,
           s.rating,
           s.status,
           s.IMDb,
           s.note
      FROM show AS s
     ORDER BY s.rating DESC,
              s.name;


